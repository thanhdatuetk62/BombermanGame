package uet.oop.bomberman.library;

public class Pair 
{
    int x, y;

    public Pair(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
